void print_prime_to_index()
{
	unsigned int i, count, limit = 20;
	unsigned int oldp;

	printf("int prime_to_index[MAXPRIME] = {\n");

	/* for all small primes */
	oldp = primes[0];
	count = 0;
	printf("-1, -1, -1, ");	/* 0, 1, 2, composite have no corresponce */

	for (i = 0; i < MAXPRIMEINDEX; i++) {

		unsigned int j;
		unsigned int p = primes[i];

		for (j = oldp + 1; j < p; j++) {
			count++;
			printf(", -1");	/* composite have no corresponce */

			if (count >= limit) {
				printf("\n");
				limit += 20;
			}
		}

		printf(", %d", i);
		count++;
		oldp = p;

		if (count >= limit) {
			printf("\n");
			limit += 20;
		}
	}

	printf("\n};\n");

	return;
}


void print_phi_divisors()
{
	unsigned int i, first = 1;

	/* for all small primes */
	printf("struct divisors_list phi_divisors[MAXPRIMEINDEX] = {");
	for (i = 0; i < MAXPRIMEINDEX; i++) {

		unsigned int p = primes[i];

		/* p is supposed to be a prime, so phi(p) = p-1 */
		unsigned int phi = p - 1;

		unsigned int d, count = 0;
		unsigned int innerfirst = 1;

		if ( ! first ) { printf(",\n"); }
		first = 0;

		printf("\t{ {");

		/* naive trial division */
		for (d = phi; d > 0; d--) {

			if ( (phi % d) == 0) {

				if ( ! innerfirst) { printf(", "); }

				innerfirst = 0;

				printf("%d", d);

				count++;
			}
		}

		printf("}, %d }", count);

	}

	printf("\n};\n");

	return;
}

